                '&ver=' + encodeURIComponent(selectedVersion) +
                '&type=' + encodeURIComponent(selectedType) +
                '&title=' + encodeURIComponent($('#snippet-title').val()) +
                '&desc=' + encodeURIComponent($('#snippet-description').val()) +
